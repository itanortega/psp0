    private void Btn_AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_AgregarActionPerformed
        if(Txt_Num.getText().trim().isEmpty()){
            Utilidades.mostrarMensaje(this, "No ha ingresado un dato válido", "Advertencia", 3);
        }else{
            numeros[datos] = Double.valueOf(Txt_Num.getText().trim());
            datos ++;
            mostrarNumeros();
            activarDesactivarBotonCalcular();
            Txt_Num.setText("");
        }
    }//GEN-LAST:event_Btn_AgregarActionPerformed

