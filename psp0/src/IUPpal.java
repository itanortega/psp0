    private void activarDesactivarBotonCalcular() {
        if(datos == 10){
            Btn_Calcular.setEnabled(true);
            Btn_Agregar.setEnabled(false);
        }else{
            Btn_Calcular.setEnabled(false);  
            Btn_Agregar.setEnabled(true);          
        }
    }

    private void mostrarNumeros() {
        String r = "";
        for(int i = 0; i<datos; i++){
            r += String.valueOf(numeros[i]) + "<br>";
        }
        Lbl_Numeros.setText("<html><body>"+r+"</body></html>");
    }
    
    private void Calcular() {
        double suma = 0;
        for(int i = 0; i<datos; i++){
            suma += numeros[i];
        }
        Utilidades.mostrarMensaje(this, "El promedio de los números ingresados es: "+String.valueOf(suma/datos), "Promedio", 1);
    }
